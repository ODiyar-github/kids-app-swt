/**
 * @enum GenderEnum
 * @description Definiert die möglichen Geschlechtsangaben für Benutzer.
 * Dies wird verwendet, um eine standardisierte Auswahl für Geschlechtsinformationen bereitzustellen.
 */
export enum GenderEnum {
  /**
   * @member {string} Male - Repräsentiert das männliche Geschlecht.
   */
  Male = 'male',

  /**
   * @member {string} Female - Repräsentiert das weibliche Geschlecht.
   */
  Female = 'female',

  /**
   * @member {string} Diverse - Repräsentiert ein diverses Geschlecht oder eine andere Geschlechtsidentität.
   */
  Diverse = 'diverse',
}
